-- Opportunity Tracker V2 user-managed fields, scanner protection, and activity.
-- Existing market/scanner columns are deliberately left untouched.

alter table public.buying_opportunities
  add column if not exists seen boolean not null default false,
  add column if not exists notes text,
  add column if not exists status text default 'New',
  add column if not exists favourite boolean not null default false,
  add column if not exists hidden boolean not null default false,
  add column if not exists last_seen timestamptz,
  add column if not exists updated_at timestamptz default now();

-- Remove the earlier V2 constraint before converting its values.
alter table public.buying_opportunities
  drop constraint if exists buying_opportunities_status_check;

-- "Seen" is now a workflow status. Preserve any state captured by the earlier
-- checkbox model when this migration is run over an existing V2 table.
update public.buying_opportunities
set status = 'Seen'
where seen = true and coalesce(status, 'New') = 'New';

-- Convert the former overlapping Hidden status into the dedicated visibility
-- flag before narrowing status to workflow-only values.
update public.buying_opportunities
set hidden = true, status = 'New'
where status = 'Hidden';

alter table public.buying_opportunities
  add constraint buying_opportunities_status_check
  check (
    status is null or status in (
      'New',
      'Seen',
      'Researching',
      'Contacted',
      'Negotiating',
      'Purchased',
      'Rejected'
    )
  );

